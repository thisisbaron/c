# 以公鸡为突破点，公鸡5元一只，100元最多只能买20只，
# 母鸡每只3元，100全拿来买母鸡，最多也不能超过33只
# 设公鸡数为x，母鸡数为y，小鸡z只
count = 0
for x in range(0, 20):
    for y in range(0, 33):
        z = 100 - x - y
        if z > 0 and 5 * x + 3 * y + z / 3 == 100:
            count += 1
            print("=" * 60)
            print(f"第{count}种买法，公鸡买了{x}只，母鸡买了{y}只，小鸡买了{z}只")
